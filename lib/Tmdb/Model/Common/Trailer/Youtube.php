namespace Tmdb\Model\Common\Trailer;

use Tmdb\Model\Common\AbstractTrailer;

class Youtube extends AbstractTrailer {

    const URL = 'http://www.youtube.com/watch?v=%s';

    private $name;
    private $size;
    private $source;
    private $type;

    public static $_properties = array(
        'name',
        'size',
        'source',
        'type'
    );

    /**
     * Retrieve the url to the source
     *
     * @todo add bonus hd=1 query parameters, but we'd need some easy way of configuring this behaviour
     *
     * @return string
     */
    public function getUrl()
    {
        return sprintf(self::URL, $this->source);
    }

    /**
     * @param mixed $name
     * @return $this
     */
    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $size
     * @return $this
     */
    public function setSize($size)
    {
        $this->size = $size;
        return $this;
    }

    /**
     * @return mixed
     */
    public function getSize()
    {
        return $this->size;
    }

    /**
     * @param mixed $source
     * @return $this
     */
    public function setSource($source)
    {
        $this->source = $source;
        return $this;
    }

    /**
     * @return mixed
     */
    public function getSource()
    {
        return $this->source;
    }

    /**
     * @param mixed $type
     * @return $this
     */
    public function setType($type)
    {
        $this->type = $type;
        return $this;
    }

    /**
     * @return mixed
     */
    public function getType()
    {
        return $this->type;
    }
}
