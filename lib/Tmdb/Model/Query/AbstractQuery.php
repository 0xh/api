namespace Tmdb\Model;

use Tmdb\Client;

abstract class AbstractQuery extends AbstractModel {

    private $client;

    public function __construct(Client $client)
    {
        $this->setClient($client);
    }

    /**
     * @param mixed $client
     * @return $this
     */
    public function setClient($client)
    {
        $this->client = $client;
        return $this;
    }

    /**
     * @return Client
     */
    public function getClient()
    {
        return $this->client;
    }
}
